// Reloads page and resets inputs
function reloadPage(){
	location.reload();
	document.getElementById('item').value = "";
	var boxes = document.getElementsByClassName('gradeBox');
	for(var i = 0; i < boxes.length; i++){
		if(!boxes[i].checked){
			boxes[i].checked = true;
		}
	}
}

