// Prints current checkbox values
function checkBoxes(){
	
	var boxes = document.getElementsByClassName('gradeBox');
	for (var i = 0; i < boxes.length; i++){
		boxes[i].addEventListener('click', function(){
			printBoxes(boxes);
		});
	}
}

function printBoxes(boxes){
	if(flag){
		checkArray = [];
		flag = !flag;
	}
	for (var i = 0; i < boxes.length; i++){
		if(boxes[i].checked){
			checkArray.push(parseInt(boxes[i].value));
		}
	}
	clearList();
	
	addItemToList();
	
	checkArray = [];
}

// Re-renders list depending on search value
function searchList(field) {
	globalSearchValue = field.value.toLowerCase();;
	clearList();
	var boxes = document.getElementsByClassName('gradeBox');
	printBoxes(boxes);
}


// Map zooms into coordinates of search value
function zoomOnSearch(){
	var searchValue = document.getElementById('item').value;
	if(searchValue) {
		var numberReturned = 0;
		var lt = 0;
		var ln = 0;
		for(var i = 0; i < rivers.length; i++){
			var river = rivers[i];
			if(river.title.toLowerCase().indexOf(searchValue.toLowerCase()) !== -1){
				lt = river.lat;
				ln = river.lng;
				numberReturned++;
			}
		}
		if(numberReturned == 1){
			var pos = {
				lat: lt,
				lng: ln
			};
			zoom(pos, true);
		}
	}
};

// Adds items to list - includes event listeners for li items
function addItemToList(){
	
	clearOverlays();
	var list = document.getElementById('riverList');
	var gZIndex = 50;
	var rZIndex = 0;
	
	var shape = {
		coords: [1, 1, 1, 20, 18, 20, 18, 1],
		type: 'poly'
	};

	for(var i = 0; i < rivers.length; i++){
		var river = rivers[i];
		if((checkArray.indexOf(river.grade) >= 0) && ((river.title.toLowerCase().indexOf(globalSearchValue) >= 0) || (river.river.toLowerCase().indexOf(globalSearchValue) >= 0))){
	
			var item = document.createElement('li');
			item.innerText = river.title;
			
			var buttons = document.createElement('div');
			buttons.classList.add('buttons');
			
			//var starIcon = document.createElement('button');
			//starIcon.classList.add('starIcon');
			//starIcon.innerHTML = starSVG;
			
			var linkIcon = document.createElement('button');
			linkIcon.classList.add('linkIcon');
			linkIcon.innerHTML = linkSVG;
			
			buttons.appendChild(linkIcon);
			//buttons.appendChild(starIcon);
			
			var content = "<h4>" + river.title + "</h4>";
				content += "<p>Grade " + river.grade + "</p>";
			
			var pos = {
				lat: river.lat,
				lng: river.lng
			};
			
			item.addEventListener('click', function(content, pos){
				return function(){
					openInfoWindow(content, pos, true);
					zoom(pos, true);
				}
			}('<h4>' + river.title + '</h4><p>Grade ' + river.grade + '</p>', {lat: river.lat, lng: river.lng}));
			
			item.addEventListener('mouseover', function(content, pos){
				return function(){
					openInfoWindow(content, pos, false);
				}
			}('<h4>' + river.title + '</h4><p>Grade ' + river.grade + '</p>', {lat: river.lat, lng: river.lng}));
			
			linkIcon.addEventListener('click', function(){
				window.location.href = river.url;
			});
			
			item.appendChild(buttons);
			list.appendChild(item);
			
			var zIndex = 0;
		
			if(river.marker == "greenMarker"){
				zIndex = gZIndex;
				gZIndex++;
			} else {
				zIndex = rZIndex;
				rZIndex++;
			}
			
			var latLng = new google.maps.LatLng(river.lat, river.lng); 
            
			var marker = new google.maps.Marker({
				position: latLng,
				icon: river.marker,
				shape: shape,
				zIndex: zIndex,
				url: river.url
			});
			
			var content = "<h4>" + river.title + "</h4>";
			content += "<p>Grade " + river.grade + "</p>";
			
			var infowindow = new google.maps.InfoWindow();
			google.maps.event.addListener(marker, 'mouseover', (function(marker ,content ,infowindow){ 
				return function() {
					var zIndex = marker.getZIndex();
					marker.setZIndex(google.maps.Marker.MAX_ZINDEX + 1);
					addInfoWindow(marker, content, zIndex);
				};
			})(marker, content, infowindow));
			
			markersArray.push(marker);
			marker.setMap(map);
		}
	}
	
}

// Opens an infor window at coordinates
function openInfoWindow(content, position, fromZoom){
	var infowindow = new google.maps.InfoWindow({
		disableAutoPan: true
	});
	infowindow.setContent(content);
	infowindow.setPosition(position);
	infowindow.open(map);
	
	if(!fromZoom){
		document.getElementById('riverList').addEventListener('mouseout', (function(position, infowindow){ 
			return function() {
				infowindow.close()
			};
		})(position, infowindow));
	}
}

// Removes all items from list
function clearList(){
	var list = document.getElementById('riverList');
	while(list.firstChild){
		list.removeChild(list.firstChild);
	}
}

function clearOverlays() {
  for (var i = 0; i < markersArray.length; i++ ) {
    markersArray[i].setMap(null);
  }
  markersArray.length = 0;
}

